        </div>
        <div class="enfermedades-item-sect block">
            <div class="enfermedades-item"><img src="src/img/glaucoma-angulo-abierto.svg" alt="">
                <h4>Glaucoma de ángulo abierto</h4>
                <p>El más común; hace que la presión del ojo incremente de forma gradual, dañando el nervio óptico. 
                    Su progreso es tan lento que podrías perder tu visión sin tener ningún síntoma adicional.</p>
            </div>
            <div class="enfermedades-item"><img src="src/img/glaucoma-angulo-cerrado_1.svg" alt="">
                <h4>Glaucoma de ángulo cerrado</h4>
                <p>Es poco frecuente pero causa una alteración repentina de la visión, 
                acompañado de síntomas como dolor ocular, naúseas y halos alrededor de las luces.</p>
            </div>
            <div class="enfermedades-item"><img src="src/img/glaucoma-tension-normal.svg" alt="">
                <h4>Glaucoma de tensión normal</h4>
                <p>Los ojos tienen una presión normal, sin embargo el nervio óptico se 
                daña produciendo una pérdida de la visión periférica. Su origen es desconocido.</p>
            </div>
            <div class="enfermedades-item"><img src="src/img/glaucoma-congenito.svg"  alt="">
                <h4>Glaucoma congénito</h4>
                <p>Es poco frecuente y aparece entre el nacimiento y los 3 años de edad por defectos del drenaje ocular, 
                con síntomas como ojos opacos, sensibilidad a la luz y lagrimeo excesivo.</p>
